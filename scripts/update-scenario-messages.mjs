// ============================================================
// メイン処理
// ============================================================
async function main() {
  console.log('============================================================');
  console.log('シナリオメッセージ本文更新');
  console.log(`モード: ${DRY_RUN ? 'ドライラン' : '本番実行'}`);
  console.log('============================================================');

  // シナリオ一覧取得
  const scenRes = await api('GET', `/api/scenarios?lineAccountId=${ACCOUNT_ID}`);
  const scenarios = scenRes.data || [];
  console.log(`シナリオ数: ${scenarios.length}`);

  let updated = 0;
  let skipped = 0;
  let failed = 0;

  for (const scenario of scenarios) {
    const messages = MESSAGES[scenario.name];
    if (!messages) {
      console.log(`SKIP: ${scenario.name} (メッセージ定義なし)`);
      skipped++;
      continue;
    }

    console.log(`\n--- ${scenario.name} ---`);

    // ステップ取得
    const detailRes = await api('GET', `/api/scenarios/${scenario.id}`);
    const steps = detailRes.data?.steps || [];

    for (const step of steps) {
      const newMessage = messages[step.stepOrder];
      if (!newMessage) {
        console.log(`  SKIP: step ${step.stepOrder} (メッセージ定義なし)`);
        skipped++;
        continue;
      }

      // プレースホルダーかどうかチェック
      const isPlaceholder = step.messageContent?.startsWith('[要設定:');
      if (!isPlaceholder && step.messageContent !== newMessage) {
        console.log(`  SKIP: step ${step.stepOrder} (既にカスタムメッセージ設定済み)`);
        skipped++;
        continue;
      }

      try {
        await api('PUT', `/api/scenarios/${scenario.id}/steps/${step.id}`, {
          messageContent: newMessage,
        });
        console.log(`  OK: step ${step.stepOrder} (${newMessage.slice(0, 30)}...)`);
        updated++;
      } catch (e) {
        console.error(`  FAIL: step ${step.stepOrder} - ${e.message}`);
        failed++;
      }
    }
  }

  console.log('\n=== サマリー ===');
  console.log(`更新: ${updated} / スキップ: ${skipped} / 失敗: ${failed}`);
}

main().catch(e => { console.error('致命的エラー:', e); process.exit(1); });
